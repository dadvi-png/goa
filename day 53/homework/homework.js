let greet2 = `hello i am ${firstName} ${lastname} and i live in ${city}`
// დაბეჭდე ორივე კონსოლში და დააკვირდი, რომელი სინტაქსი უფრო მოსახერხებელი და სუფთაა.
console.log(greet1)
console.log(greet2)