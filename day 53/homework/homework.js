// შექმენი ცვლადი let-ის გამოყენებით შენი ასაკისთვის (მაგ: myAge) და მიანიჭე შესაბამისი რიცხვითი მნიშვნელობა.
let myAge = 12;
// შექმენი ცვლადი const-ის გამოყენებით შენი დაბადების წლისთვის (მაგ: birthYear) და მიანიჭე მნიშვნელობა.
const birthYear = 2014;
// ახლა სცადე შეცვალო myAge-ის მნიშვნელობა ახალი რიცხვით.
