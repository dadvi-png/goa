class MathHelper{
static average =(numbers) =>{
    let sum = 0
    let length = numbers.length
    for(let i of numbers){
        sum+=i
    }
    return sum / length
}
static iseven = (num1) =>{
    if(num1 % 2 === 0){
        return true
    }
    else{
        return false
    }
}
}
